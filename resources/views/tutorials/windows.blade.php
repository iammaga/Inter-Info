    <h3>Why is Microsoft Windows called Windows?</h3>

    <p>Before the release of Microsoft Windows, Microsoft users were used to the single task command line operating system MS-DOS. Because Microsoft names most of its products with one word, it needed a word that best described its new GUI operating system. Microsoft chose "Windows" because of the multiple windows that allow different tasks and programs to run at the same time. Because you cannot trademark a common name like "Windows," it's officially known as "Microsoft Windows". The first version of Microsoft Windows was version 1.0, released in 1985.</p>

